//EQUAL OPERATOR
console.log("3" == 3);
/* Coercion is the process of converting one type to another
 */
//STRICT EQUAL
console.log("3" === 3); // overrides Coercion to check data type as well

//Not EQUAL
console.log("3" != 3);

// STRICT NOT EQUAL
console.log("3" !== 3);

//GREATER THAN
console.log(3 > 2);

//LESS THAN
console.log(3 < 2);

// GREATER THAN OR EQUAL
console.log(3 >= 2);

// LESS THAN OR EQUAL
console.log(3 <= 2);

// AND
2 && 3;
//OR
3 || 2;
